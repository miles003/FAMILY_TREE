     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $users = User::with('father', 'mother', 'wife', 'husband', 'childs')->where('id', $request->users ?? auth()->user()->id)->first();
        $users['siblings'] = $users->siblings();
        $users['authenticated_id'] = auth()->user()->id;
        $users['grandFatherToMyFather'] = $users->grandFatherToMyFather();
        $users['grandMotherToMyFather'] = $users->grandMotherToMyFather();
        $users['grandFatherToMyMother'] = $users->grandFatherToMyMother();
        $users['grandMotherToMyMother'] = $users->grandMotherToMyMother();
        return $users;
    }

